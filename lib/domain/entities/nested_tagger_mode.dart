/// 중첩 워크스페이스(자체 `.filetagger/`를 가진 하위 폴더)에 대해 사용자가
/// 확정한 **지속 상태**. 이 기록의 유일한 역할은 이후 스캔에서 병합 프롬프트를
/// 반복하지 않도록 하는 것이다. 폴더의 실제 동작(내부 인덱싱 여부)은 폴더 노드의
/// `FolderManageMode` override가 정하므로 여기에 중복 표현하지 않는다.
///
/// "흡수"는 지속 상태가 아니라 1회성 동작이라 이 enum에 없다. 흡수 후 하위 태거를
/// 남기면 [ignore]로 기록하고, 제거하면 남는 중첩이 없어 기록하지 않는다.
///
/// 이름 기반 저장(Drift `textEnum`)이라 값 순서가 바뀌어도 영향받지 않는다.
enum NestedTaggerMode {
  /// 하위 태거를 내부 미탐색 단일 노드로 둔다(폴더는 불투명).
  independent,

  /// 하위 태거를 무시하고 내부를 상위 규칙으로 인덱싱한다(폴더는 재귀 관리).
  ignore,
}
