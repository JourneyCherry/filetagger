/// 태그값을 어떻게 해석할지 결정하는 유형.
///
/// 입력 위젯, 검증 규칙, 정렬 비교 로직이 이 유형에 따라 달라진다
/// (number=숫자 비교, date=시간순, text=사전순, label=값 없는 분류용,
/// link=대상 노드를 가리키며 그 이름으로 비교·표시).
/// enum 미채택 결정에 따라 향후 유형을 추가할 여지를 둔다.
enum TagValueType {
  /// 값 없이 부착 여부만 갖는 분류용 태그.
  label,

  /// 자유 텍스트 값.
  text,

  /// 숫자 값.
  number,

  /// 날짜/시각 값.
  date,

  /// 워크스페이스 안의 다른 노드를 가리키는 링크. **저장값은 대상 노드 id**이고,
  /// 표시·비교·그룹은 대상 노드의 **이름**을 쓴다(이름 해석은 소비 계층이 주입).
  /// 지정한 링크 태그의 대상 이미지를 노드 썸네일로 쓰는 등 앱 동작에 이어진다.
  link,

  /// 워크스페이스 **밖**의 이미지 파일을 등록해 쓰는 커스텀 이미지. 부여 시 외부
  /// 파일을 골라 `.filetagger/` 캐시(내용 해시로 중복 제거, 큰 이미지는 축소)에
  /// 넣고, **저장값은 캐시 키**(파일명)다. 캐시 파일은 노드가 아니며 사용자가 직접
  /// 다루지 않는다. link와 마찬가지로 지정한 이미지 태그를 노드 썸네일 출처로 쓸
  /// 수 있다. 값이 불투명한 해시라 필터·정렬·그룹은 label처럼 **존재 여부만** 본다.
  image,
}
