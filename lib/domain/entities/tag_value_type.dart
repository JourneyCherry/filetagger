/// 태그값을 어떻게 해석할지 결정하는 유형.
///
/// 입력 위젯, 검증 규칙, 정렬 비교 로직이 이 유형에 따라 달라진다
/// (number=숫자 비교, date=시간순, text=사전순, label=값 없는 분류용).
/// enum 미채택 결정에 따라 향후 유형을 추가할 여지를 둔다.
enum TagValueType {
  /// 값 없이 부착 여부만 갖는 분류용 태그.
  label,

  /// 자유 텍스트 값.
  text,

  /// 숫자 값.
  number,

  /// 날짜/시각 값.
  date,
}
